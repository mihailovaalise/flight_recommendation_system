bce_loss = nn.BCEWithLogitsLoss()

def run_epoch(model, X, y, optimizer=None, criterion=None, batch_size=4096, train=True):
    if criterion is None:
        criterion = nn.BCEWithLogitsLoss()

    if train:
        model.train()
    else:
        model.eval()

    n = len(X)
    indices = np.arange(n)

    if train:
        np.random.shuffle(indices)

    total_loss = 0.0
    all_probs = []

    for start in range(0, n, batch_size):
        batch_idx = indices[start:start + batch_size]

        xb = torch.tensor(X[batch_idx], dtype=torch.float32, device=device)
        yb = torch.tensor(y[batch_idx], dtype=torch.float32, device=device)

        if train:
            optimizer.zero_grad()

            logits = model(xb).squeeze(-1)
            loss = criterion(logits, yb)

            loss.backward()
            optimizer.step()
        else:
            with torch.no_grad():
                logits = model(xb).squeeze(-1)
                loss = criterion(logits, yb)

        probs = torch.sigmoid(logits).detach().cpu().numpy()
        all_probs.append(probs)

        total_loss += loss.item() * len(batch_idx)

    avg_loss = total_loss / n
    all_probs = np.concatenate(all_probs)

    return avg_loss, all_probs

