def smoothed_distribution(values, vocab, alpha=1.0):
    counts = pd.Series(values).value_counts().to_dict()
    total = sum(counts.values())
    K = len(vocab)

    dist = {}
    for v in vocab:
        c = counts.get(v, 0)
        dist[v] = (c + alpha) / (total + alpha * K)
    return dist

global_feature_dist = {
    col: smoothed_distribution(
        train_cb[col].astype(str).tolist(),
        feature_vocab[col],
        alpha=ALPHA_SMOOTH
    )
    for col in flight_feature_cols
}

