def fit_logistic_regression(X_train, y_train, X_val, y_val):
    preprocessor = ColumnTransformer(
        transformers=[
            (
                "num",
                Pipeline([
                    ("imputer", SimpleImputer(strategy="constant", fill_value=0)),
                    ("scaler", StandardScaler()),
                ]),
                numeric_cols
            ),
            (
                "cat",
                Pipeline([
                    ("imputer", SimpleImputer(strategy="constant", fill_value="UNK")),
                    ("onehot", OneHotEncoder(handle_unknown="ignore")),
                ]),
                categorical_cols
            ),
        ]
    )

    model = Pipeline([
        ("prep", preprocessor),
        ("clf", LogisticRegression(
            max_iter=1000,
            class_weight="balanced",
            random_state=SEED
        ))
    ])

    model.fit(X_train, y_train)
    val_scores = model.predict_proba(X_val)[:, 1]
    return model, val_scores

def fit_random_forest(X_train, y_train, X_val, y_val):
    preprocessor = ColumnTransformer(
        transformers=[
            (
                "num",
                Pipeline([
                    ("imputer", SimpleImputer(strategy="constant", fill_value=0)),
                ]),
                numeric_cols
            ),
            (
                "cat",
                Pipeline([
                    ("imputer", SimpleImputer(strategy="constant", fill_value="UNK")),
                    ("onehot", OneHotEncoder(handle_unknown="ignore")),
                ]),
                categorical_cols
            ),
        ]
    )

    model = Pipeline([
        ("prep", preprocessor),
        ("clf", RandomForestClassifier(
            n_estimators=150,
            max_depth=10,
            min_samples_leaf=5,
            class_weight="balanced_subsample",
            random_state=SEED,
            n_jobs=-1
        ))
    ])

    model.fit(X_train, y_train)
    val_scores = model.predict_proba(X_val)[:, 1]
    return model, val_scores

def fit_catboost_classifier(X_train, y_train, X_val, y_val):
    X_tr = X_train.copy()
    X_va = X_val.copy()

    for c in categorical_cols:
        X_tr[c] = X_tr[c].fillna("UNK").astype(str)
        X_va[c] = X_va[c].fillna("UNK").astype(str)

    for c in numeric_cols:
        X_tr[c] = pd.to_numeric(X_tr[c], errors="coerce").fillna(0)
        X_va[c] = pd.to_numeric(X_va[c], errors="coerce").fillna(0)

    cat_feature_idx = [X_tr.columns.get_loc(c) for c in categorical_cols]

    model = CatBoostClassifier(
        loss_function="Logloss",
        eval_metric="AUC",
        iterations=250,
        learning_rate=0.05,
        depth=6,
        l2_leaf_reg=5,
        random_seed=SEED,
        verbose=100,
        auto_class_weights="Balanced"
    )

    model.fit(
        X_tr,
        y_train,
        cat_features=cat_feature_idx,
        eval_set=(X_va, y_val),
        use_best_model=True
    )

    val_scores = model.predict_proba(X_va)[:, 1]
    return model, val_scores

