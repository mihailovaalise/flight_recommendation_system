def evaluate_full_ranking(model, df_eval, train_interactions, topk_list=(5, 10), max_users=2000):
    model.eval()

    user_pos_train = train_interactions.groupby("passenger_id")["route_id"].apply(set).to_dict()
    user_pos_eval = df_eval.groupby("passenger_id")["route_id"].apply(set).to_dict()

    users_eval = list(user_pos_eval.keys())
    users_eval = [u for u in users_eval if u in user2idx]

    if len(users_eval) > max_users:
        users_eval = list(np.random.choice(users_eval, max_users, replace=False))

    with torch.no_grad():
        emb = model(edge_index)

        route_nodes_tensor = torch.tensor(
            [route_node(r) for r in all_routes],
            dtype=torch.long,
            device=device
        )
        route_emb = emb[route_nodes_tensor]

        metrics = {}
        per_k = {k: {"hitrate": [], "ndcg": []} for k in topk_list}
        mrrs = []
        recommended_routes_at_10 = set()

        for u in tqdm(users_eval, desc="FULL EVAL"):
            u_emb = emb[user_node(u)]
            scores = torch.matmul(route_emb, u_emb).detach().cpu().numpy()

            seen_train = user_pos_train.get(u, set())
            true_routes = user_pos_eval.get(u, set())

            candidate_mask = np.array([r not in seen_train for r in all_routes])
            candidate_routes = np.array(all_routes)[candidate_mask]
            candidate_scores = scores[candidate_mask]

            if len(candidate_routes) == 0 or len(true_routes) == 0:
                continue

            y_true = np.array([1 if r in true_routes else 0 for r in candidate_routes], dtype=int)
            y_score = candidate_scores

            order = np.argsort(-y_score)
            ranked_routes = candidate_routes[order]

            for k in topk_list:
                per_k[k]["hitrate"].append(hitrate_at_k(y_true, y_score, k))
                per_k[k]["ndcg"].append(ndcg_at_k(y_true, y_score, k))

            top10 = ranked_routes[:10]
            recommended_routes_at_10.update(top10)

            mrrs.append(mrr_score(y_true, y_score))

        for k in topk_list:
            metrics[f"FULL_HitRate@{k}"] = float(np.mean(per_k[k]["hitrate"])) if per_k[k]["hitrate"] else np.nan
            metrics[f"FULL_NDCG@{k}"] = float(np.mean(per_k[k]["ndcg"])) if per_k[k]["ndcg"] else np.nan

        metrics["FULL_MRR"] = float(np.mean(mrrs)) if mrrs else np.nan
        metrics["Coverage@10"] = (
            float(len(recommended_routes_at_10) / len(all_routes))
            if len(all_routes) > 0 else np.nan
        )

    return metrics

