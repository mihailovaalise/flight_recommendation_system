# 5. Метрики для full evaluation
def dcg_at_k(y_true_sorted, k):
    y = np.asarray(y_true_sorted[:k], dtype=float)
    if y.size == 0:
        return 0.0
    discounts = np.log2(np.arange(2, y.size + 2))
    return float(np.sum(y / discounts))

def ndcg_at_k(y_true, y_score, k=10):
    order = np.argsort(-y_score)
    y_sorted = y_true[order]

    dcg = dcg_at_k(y_sorted, k)
    ideal = np.sort(y_true)[::-1]
    idcg = dcg_at_k(ideal, k)

    if idcg == 0:
        return 0.0
    return float(dcg / idcg)

def hitrate_at_k(y_true, y_score, k=10):
    order = np.argsort(-y_score)[:k]
    return float(np.sum(y_true[order]) > 0)

def mrr_score(y_true, y_score):
    order = np.argsort(-y_score)
    y_sorted = y_true[order]
    pos = np.where(y_sorted == 1)[0]
    if len(pos) == 0:
        return 0.0
    return float(1.0 / (pos[0] + 1))

# 6. FULL evaluation reranker поверх LightGCN-кандидатов
def evaluate_full_with_reranker_final(df_eval, topN=50, topk_list=[5, 10], max_users=2000):
    users = df_eval["passenger_id"].unique()
    users = [u for u in users if u in user2idx]

    if max_users is not None and len(users) > max_users:
        users = list(np.random.choice(users, max_users, replace=False))

    df_eval_sub = df_eval[df_eval["passenger_id"].isin(users)].copy()

    user_true = df_eval_sub.groupby("passenger_id")["route_id"].apply(set).to_dict()
    train_seen = train_route_df.groupby("passenger_id")["route_id"].apply(set).to_dict()

    metrics = {}
    per_k_hits = {k: [] for k in topk_list}
    per_k_ndcgs = {k: [] for k in topk_list}
    mrrs = []
    recommended_routes_at_10 = set()

    for u in users:
        true_routes = user_true.get(u, set())
        if len(true_routes) == 0:
            continue

        scores = get_lightgcn_scores_for_user(u)
        if scores is None:
            continue

        seen = train_seen.get(u, set())

        route_score_pairs = [(r, s) for r, s in zip(all_routes, scores) if r not in seen]
        if len(route_score_pairs) == 0:
            continue

        route_score_pairs.sort(key=lambda x: x[1], reverse=True)
        N = min(topN, len(route_score_pairs))

        candidate_routes = np.array([r for r, _ in route_score_pairs[:N]])
        if len(candidate_routes) == 0:
            continue

        rerank_scores = predict_reranker_scores_for_user_final(u, candidate_routes)

        y_true = np.array([1 if r in true_routes else 0 for r in candidate_routes], dtype=int)
        y_score = rerank_scores

        for k in topk_list:
            per_k_hits[k].append(hitrate_at_k(y_true, y_score, k))
            per_k_ndcgs[k].append(ndcg_at_k(y_true, y_score, k))

        mrrs.append(mrr_score(y_true, y_score))

        order = np.argsort(-y_score)
        ranked_routes = candidate_routes[order]
        top10 = ranked_routes[:10]
        recommended_routes_at_10.update(top10)

    for k in topk_list:
        metrics[f"FULLHitRate{k}"] = float(np.mean(per_k_hits[k])) if per_k_hits[k] else np.nan
        metrics[f"FULLNDCG{k}"] = float(np.mean(per_k_ndcgs[k])) if per_k_ndcgs[k] else np.nan

    metrics["FULLMRR"] = float(np.mean(mrrs)) if mrrs else np.nan
    metrics["Coverage10"] = (
        float(len(recommended_routes_at_10) / len(all_routes))
        if len(all_routes) > 0 else np.nan
    )

    return metrics

