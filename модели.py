class SimpleFeatureEncoder(nn.Module):
    def __init__(self, cat_dims, feat_dim=64):
        super().__init__()
        self.sex_emb = nn.Embedding(cat_dims[0], 4)
        self.dep_air_emb = nn.Embedding(cat_dims[1], 8)
        self.arr_air_emb = nn.Embedding(cat_dims[2], 8)

        self.num_proj = nn.Sequential(
            nn.Linear(3, feat_dim),
            nn.ReLU(),
            nn.Linear(feat_dim, feat_dim)
        )

        self.out_dim = 4 + 8 + 8 + feat_dim

    def forward(self, all_cat, all_num):
        sex_ids = all_cat[:, 0]
        dep_ids = all_cat[:, 1]
        arr_ids = all_cat[:, 2]

        sex_e = self.sex_emb(sex_ids)
        dep_e = self.dep_air_emb(dep_ids)
        arr_e = self.arr_air_emb(arr_ids)

        x_num = self.num_proj(all_num)
        x = torch.cat([sex_e, dep_e, arr_e, x_num], dim=1)
        return x


class GCNFeatRec(nn.Module):
    def __init__(self, cat_dims, num_nodes, emb_dim=64, n_layers=2):
        super().__init__()
        # ID-эмбеддинги (как в обычном GCN)
        self.id_emb = nn.Embedding(num_nodes, emb_dim)
        nn.init.normal_(self.id_emb.weight, std=0.1)

        # фичи
        self.encoder = SimpleFeatureEncoder(cat_dims=cat_dims, feat_dim=emb_dim)
        feat_out_dim = self.encoder.out_dim

        # склеиваем id-эмбеддинг и фичи
        self.fuse = nn.Linear(emb_dim + feat_out_dim, emb_dim)

        from torch_geometric.nn import GCNConv
        self.convs = nn.ModuleList([GCNConv(emb_dim, emb_dim) for _ in range(n_layers)])

    def forward(self, edge_index):
        id_e = self.id_emb.weight                       # [num_nodes, emb_dim]
        feat_e = self.encoder(all_cat, all_num)         # [num_nodes, feat_out_dim]
        x0 = torch.cat([id_e, feat_e], dim=1)           # [num_nodes, emb_dim + feat_out_dim]
        x0 = self.fuse(x0)                              # [num_nodes, emb_dim]

        x = x0
        for i, conv in enumerate(self.convs):
            x = conv(x, edge_index)
            if i != len(self.convs) - 1:
                x = F.relu(x)
        return x


class GraphSAGEFeatRec(nn.Module):
    def __init__(self, cat_dims, num_nodes, emb_dim=64, n_layers=2):
        super().__init__()
        self.id_emb = nn.Embedding(num_nodes, emb_dim)
        nn.init.normal_(self.id_emb.weight, std=0.1)

        self.encoder = SimpleFeatureEncoder(cat_dims=cat_dims, feat_dim=emb_dim)
        feat_out_dim = self.encoder.out_dim
        self.fuse = nn.Linear(emb_dim + feat_out_dim, emb_dim)

        from torch_geometric.nn import SAGEConv
        self.convs = nn.ModuleList([SAGEConv(emb_dim, emb_dim) for _ in range(n_layers)])

    def forward(self, edge_index):
        id_e = self.id_emb.weight
        feat_e = self.encoder(all_cat, all_num)
        x0 = torch.cat([id_e, feat_e], dim=1)
        x0 = self.fuse(x0)

        x = x0
        for i, conv in enumerate(self.convs):
            x = conv(x, edge_index)
            if i != len(self.convs) - 1:
                x = F.relu(x)
        return x

