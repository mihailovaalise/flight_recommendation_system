def train_model(model, name, epochs=5, batch_size=4096, lr=1e-3):
    model = model.to(device)
    opt = torch.optim.Adam(model.parameters(), lr=lr)

    n = len(bpr_u)

    for epoch in range(1, epochs + 1):
        model.train()
        perm = torch.randperm(n, device=device)
        total_loss = 0.0

        for start in tqdm(range(0, n, batch_size), desc=f"{name} epoch {epoch}"):
            idx = perm[start:start + batch_size]

            opt.zero_grad()

            all_emb = model(edge_index)

            u = all_emb[bpr_u[idx]]
            p = all_emb[bpr_p[idx]]
            n_emb = all_emb[bpr_n[idx]]

            pos = (u * p).sum(1)
            neg = (u * n_emb).sum(1)

            loss = -torch.log(torch.sigmoid(pos - neg) + 1e-12).mean()

            loss.backward()
            opt.step()

            total_loss += loss.item() * len(idx)

        print(f"[{name}] epoch {epoch:02d} | loss = {total_loss / n:.6f}")

    return model

# 10. PREDICT FOR SAMPLED EVAL
def predict_sampled(model, df_rank):
    model.eval()
    scores = np.zeros(len(df_rank), dtype=np.float32)

    mask = (
        df_rank['passenger_id'].isin(user2idx) &
        df_rank['route_id'].isin(route2idx)
    )

    sub = df_rank[mask]

    if len(sub) == 0:
        return scores

    u_nodes = torch.tensor(
        [user_node(u) for u in sub['passenger_id']],
        dtype=torch.long,
        device=device
    )
    r_nodes = torch.tensor(
        [route_node(r) for r in sub['route_id']],
        dtype=torch.long,
        device=device
    )

    with torch.no_grad():
        emb = model(edge_index)
        s = (emb[u_nodes] * emb[r_nodes]).sum(1)
        scores[mask.values] = torch.sigmoid(s).detach().cpu().numpy()

    return scores

# 11. RANKING METRICS
def dcg_at_k(y_true_sorted, k):
    y = np.asarray(y_true_sorted[:k], dtype=float)
    if y.size == 0:
        return 0.0
    discounts = np.log2(np.arange(2, y.size + 2))
    return float(np.sum(y / discounts))


def ndcg_at_k(y_true, y_score, k=10):
    order = np.argsort(-y_score)
    y_sorted = y_true[order]

    dcg = dcg_at_k(y_sorted, k)
    ideal = np.sort(y_true)[::-1]
    idcg = dcg_at_k(ideal, k)

    if idcg == 0:
        return 0.0
    return float(dcg / idcg)


def hitrate_at_k(y_true, y_score, k=10):
    order = np.argsort(-y_score)[:k]
    return float(np.sum(y_true[order]) > 0)


def mrr_score(y_true, y_score):
    order = np.argsort(-y_score)
    y_sorted = y_true[order]
    pos = np.where(y_sorted == 1)[0]

    if len(pos) == 0:
        return 0.0

    return float(1.0 / (pos[0] + 1))

# 12. SAMPLED EVALUATION
def evaluate_ranking(df_rank, scores, topk_list=(5, 10)):
    df_rank = df_rank.copy()
    df_rank["score"] = scores

    metrics = {}
    grouped = list(df_rank.groupby("passenger_id"))

    for k in topk_list:
        hits = []
        ndcgs = []

        for _, grp in grouped:
            y_g = grp["label"].values
            s_g = grp["score"].values

            hits.append(hitrate_at_k(y_g, s_g, k))
            ndcgs.append(ndcg_at_k(y_g, s_g, k))

        metrics[f"HitRate@{k}"] = float(np.mean(hits)) if hits else np.nan
        metrics[f"NDCG@{k}"] = float(np.mean(ndcgs)) if ndcgs else np.nan

    mrrs = []
    for _, grp in grouped:
        y_g = grp["label"].values
        s_g = grp["score"].values
        mrrs.append(mrr_score(y_g, s_g))

    metrics["MRR"] = float(np.mean(mrrs)) if mrrs else np.nan
    return metrics

