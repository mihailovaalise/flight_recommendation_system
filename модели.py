match_feature_cols = ['route_id'] + flight_feature_cols

def make_signature_from_row(row, cols):
    return tuple(str(row[c]) for c in cols)

def evaluate_flight_ranker(
    df_targets,
    route_to_candidates,
    user_profiles,
    global_dist,
    weights,
    k_list=(1, 3, 5, 10),
    max_users=5000,
    per_user_last_only=True
):
    df_eval = df_targets.copy()

    if per_user_last_only:
        df_eval = (
            df_eval
            .sort_values(['passenger_id', 'Дата бронирования'])
            .groupby('passenger_id')
            .tail(1)
            .reset_index(drop=True)
        )

    users = df_eval['passenger_id'].unique()
    if max_users is not None and len(users) > max_users:
        users = np.random.choice(users, max_users, replace=False)
        df_eval = df_eval[df_eval['passenger_id'].isin(users)].reset_index(drop=True)

    print("Eval rows:", len(df_eval), "Eval users:", df_eval['passenger_id'].nunique())

    hits = {k: [] for k in k_list}
    ndcgs = {k: [] for k in k_list}

    for _, row in tqdm(df_eval.iterrows(), total=len(df_eval), desc="Offline eval flight ranker"):
        user_id = row['passenger_id']
        route_id = row['route_id']
        true_fid = str(row['flight_id'])

        candidates = route_to_candidates.get(route_id, None)
        if candidates is None or len(candidates) == 0:
            continue

        ranked = candidates.copy()
        ranked['score'] = ranked.apply(
            lambda x: score_flight(
                user_id=user_id,
                flight_row=x,
                user_profiles=user_profiles,
                global_dist=global_dist,
                weights=weights
            ),
            axis=1
        )
        ranked = ranked.sort_values('score', ascending=False).reset_index(drop=True)

        # exact match по flight_id
        y_true = (ranked['flight_id'].astype(str).values == true_fid).astype(int)

        # fallback на совпадение по сигнатуре признаков
        if y_true.sum() == 0:
            target_sig = make_signature_from_row(row, match_feature_cols)
            cand_sigs = [
                make_signature_from_row(r, match_feature_cols)
                for _, r in ranked.iterrows()
            ]
            y_true = np.array(
                [1 if sig == target_sig else 0 for sig in cand_sigs],
                dtype=np.int32
            )

        if y_true.sum() == 0:
            continue

        y_score = ranked['score'].values
        ranking = np.argsort(-y_score)

        for k in k_list:
            topk_idx = ranking[:k]
            hit = 1.0 if y_true[topk_idx].sum() > 0 else 0.0
            hits[k].append(hit)

            rel = y_true[ranking][:k]
            dcg = np.sum(rel / np.log2(np.arange(2, len(rel) + 2)))
            ideal = np.sort(y_true)[::-1][:k]
            idcg = np.sum(ideal / np.log2(np.arange(2, len(ideal) + 2)))
            ndcg = float(dcg / idcg) if idcg > 0 else 0.0
            ndcgs[k].append(ndcg)

    metrics = {}
    for k in k_list:
        metrics[f'HitRate@{k}'] = float(np.mean(hits[k])) if len(hits[k]) > 0 else np.nan
        metrics[f'NDCG@{k}'] = float(np.mean(ndcgs[k])) if len(ndcgs[k]) > 0 else np.nan

    return metrics

