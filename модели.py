class LightGCN(nn.Module):
    def __init__(self, num_nodes, emb_dim=64, n_layers=2):
        super().__init__()
        self.emb = nn.Embedding(num_nodes, emb_dim)
        self.n_layers = n_layers
        nn.init.normal_(self.emb.weight, std=0.1)

    def propagate(self, edge_index, x):
        row, col = edge_index
        deg = torch.bincount(row, minlength=x.size(0)).float()
        deg_inv = deg.pow(-0.5)
        deg_inv[torch.isinf(deg_inv)] = 0
        norm = deg_inv[row] * deg_inv[col]

        out = torch.zeros_like(x)
        out.index_add_(0, row, x[col] * norm.unsqueeze(1))
        return out

    def forward(self, edge_index):
        x = self.emb.weight
        all_emb = [x]
        for _ in range(self.n_layers):
            x = self.propagate(edge_index, x)
            all_emb.append(x)
        return torch.mean(torch.stack(all_emb), dim=0)

class GCNRec(nn.Module):
    def __init__(self, num_nodes, emb_dim=64, n_layers=2):
        super().__init__()
        from torch_geometric.nn import GCNConv

        self.emb = nn.Embedding(num_nodes, emb_dim)
        self.n_layers = n_layers

        self.convs = nn.ModuleList()
        if n_layers == 1:
            self.convs.append(GCNConv(emb_dim, emb_dim))
        else:
            for _ in range(n_layers):
                self.convs.append(GCNConv(emb_dim, emb_dim))

        nn.init.normal_(self.emb.weight, std=0.1)

    def forward(self, edge_index):
        x = self.emb.weight
        for i, conv in enumerate(self.convs):
            x = conv(x, edge_index)
            if i != len(self.convs) - 1:
                x = F.relu(x)
        return x

class GraphSAGERec(nn.Module):
    def __init__(self, num_nodes, emb_dim=64, n_layers=2):
        super().__init__()
        from torch_geometric.nn import SAGEConv

        self.emb = nn.Embedding(num_nodes, emb_dim)
        self.n_layers = n_layers

        self.convs = nn.ModuleList()
        if n_layers == 1:
            self.convs.append(SAGEConv(emb_dim, emb_dim))
        else:
            for _ in range(n_layers):
                self.convs.append(SAGEConv(emb_dim, emb_dim))

        nn.init.normal_(self.emb.weight, std=0.1)

    def forward(self, edge_index):
        x = self.emb.weight
        for i, conv in enumerate(self.convs):
            x = conv(x, edge_index)
            if i != len(self.convs) - 1:
                x = F.relu(x)
        return x

