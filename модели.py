def evaluate_flight_reranker_same_metrics(
    df_rank,
    score_col="score",
    k_list=(1, 3, 5, 10)
):
    df_eval = df_rank.copy()

    df_eval["query_id"] = (
        df_eval["query_passenger_id"].astype(str) + "||" +
        df_eval["query_route_id"].astype(str) + "||" +
        df_eval["target_flight_id"].astype(str)
    )

    hit_lists = {k: [] for k in k_list}
    ndcg_lists = {k: [] for k in k_list}

    for _, grp in df_eval.groupby("query_id"):
        grp = grp.sort_values(score_col, ascending=False).reset_index(drop=True)
        y_true = grp["label"].astype(int).values

        if y_true.sum() == 0:
            continue

        for k in k_list:
            top = y_true[:k]
            hit_k = 1.0 if top.sum() > 0 else 0.0
            hit_lists[k].append(hit_k)

            discounts = np.log2(np.arange(2, len(top) + 2))
            dcg = float((top / discounts).sum())

            ideal = np.sort(y_true)[::-1][:k]
            ideal_discounts = np.log2(np.arange(2, len(ideal) + 2))
            idcg = float((ideal / ideal_discounts).sum()) if len(ideal) > 0 else 0.0

            ndcg = (dcg / idcg) if idcg > 0 else 0.0
            ndcg_lists[k].append(ndcg)

    metrics = {}
    for k in k_list:
        metrics[f"HitRate@{k}"] = float(np.mean(hit_lists[k])) if hit_lists[k] else np.nan
        metrics[f"NDCG@{k}"] = float(np.mean(ndcg_lists[k])) if ndcg_lists[k] else np.nan

    return metrics

def evaluate_binary_quality(y_true, y_score):
    metrics = {}
    if len(np.unique(y_true)) > 1:
        metrics["ROC_AUC"] = float(roc_auc_score(y_true, y_score))
        metrics["PR_AUC"] = float(average_precision_score(y_true, y_score))
    else:
        metrics["ROC_AUC"] = np.nan
        metrics["PR_AUC"] = np.nan
    return metrics

