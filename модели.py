def train_lightgcn_with_early_stopping():
    model = LightGCN(num_nodes=num_nodes, emb_dim=EMB_DIM, n_layers=N_LAYERS).to(device)
    opt = torch.optim.Adam(model.parameters(), lr=BEST_LR)

    n = len(bpr_u)

    best_val = -1.0
    best_state = None
    epochs_no_improve = 0
    history = []

    for epoch in range(1, MAX_EPOCHS + 1):
        # ---------- train ----------
        model.train()
        perm = torch.randperm(n, device=device)
        total_loss = 0.0

        for start in tqdm(range(0, n, BEST_BS), desc=f"LightGCN epoch {epoch}"):
            idx = perm[start:start + BEST_BS]

            opt.zero_grad()
            loss = bpr_loss_with_reg(model, idx, reg_lambda=REG_LAMBDA)
            loss.backward()
            opt.step()

            total_loss += loss.item() * len(idx)

        train_loss = total_loss / n

        # ---------- validation ----------
        model.eval()
        with torch.no_grad():
            val_scores = predict_sampled(model, val_rank)
        val_metrics = evaluate_ranking(val_rank, val_scores, topk_list=(5, 10))

        cur_val = val_metrics.get("NDCG@10", float("nan"))
        print(f"[epoch {epoch:02d}] loss={train_loss:.5f}, val_NDCG@10={cur_val:.6f}")

        history.append({
            "epoch": epoch,
            "train_loss": train_loss,
            **{f"val_{k}": v for k, v in val_metrics.items()}
        })

        # ---------- early stopping ----------
        if not np.isnan(cur_val) and cur_val > best_val + 1e-6:
            best_val = cur_val
            best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            epochs_no_improve = 0
            print(f"  -> new best {MONITOR_METRIC} = {best_val:.6f}")
        else:
            epochs_no_improve += 1
            print(f"  no improvement for {epochs_no_improve} epochs")

        if epochs_no_improve >= PATIENCE:
            print(f"Early stopping triggered at epoch {epoch}")
            break

    # восстанавливаем лучшую модель
    if best_state is not None:
        model.load_state_dict(best_state)
    else:
        print("Warning: best_state is None, returning last epoch model")

    # сохраняем историю
    history_df = pd.DataFrame(history)
    history_df.to_csv("lightgcn_early_stopping_history.csv", index=False)
    print("Saved training history to lightgcn_early_stopping_history.csv")

    return model, history_df

