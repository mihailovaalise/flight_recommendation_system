    if len(df_local) > max_queries:
        df_local = df_local.sample(n=max_queries, random_state=seed).reset_index(drop=True)

    for _, row in tqdm(df_local.iterrows(), total=len(df_local), desc="Build reranker dataset"):
        passenger_id = str(row["passenger_id"])
        route_id = str(row["Маршрут"])
        true_flight_id = str(row["flight_id"])

        candidates = route_to_candidates.get(route_id, None)
        if candidates is None or len(candidates) == 0:
            continue

        cand_df = build_candidate_features_for_user(passenger_id, candidates)

        cand_df["query_passenger_id"] = passenger_id
        cand_df["query_route_id"] = route_id
        cand_df["target_flight_id"] = true_flight_id

        cand_df["label"] = (cand_df["flight_id"].astype(str) == true_flight_id).astype(int)

        if cand_df["label"].sum() == 0:
            continue

        rows.append(cand_df)

    if not rows:
        return pd.DataFrame()

    out_df = pd.concat(rows, ignore_index=True)
    return out_df

