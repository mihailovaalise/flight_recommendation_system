def bpr_loss_with_reg(model, idx, reg_lambda=0.0):
    all_emb = model(edge_index)

    u = all_emb[bpr_u[idx]]
    p = all_emb[bpr_p[idx]]
    n = all_emb[bpr_n[idx]]

    pos = (u * p).sum(1)
    neg = (u * n).sum(1)

    bpr_loss = -torch.log(torch.sigmoid(pos - neg) + 1e-12).mean()

    if reg_lambda > 0:
        reg_loss = (
            u.pow(2).sum(dim=1).mean() +
            p.pow(2).sum(dim=1).mean() +
            n.pow(2).sum(dim=1).mean()
        )
        bpr_loss = bpr_loss + reg_lambda * reg_loss

    return bpr_loss

