def score_flight(user_id, flight_row, user_profiles, global_dist, weights):
    prof = user_profiles.get(user_id, None)
    score = 0.0

    for col, w in weights.items():
        val = str(flight_row.get(col, "UNK"))

        if prof is None:
            p = global_dist[col].get(val, PROFILE_MIN_PROB)
        else:
            p = prof[col].get(val, PROFILE_MIN_PROB)

        p = max(p, PROFILE_MIN_PROB)
        score += w * np.log(p)

    return float(score)

