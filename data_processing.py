    if pd.isna(row['Возраст']):
        key = (row['Маршрут'], row['Класс бронирования'])
        if key in age_med_group.index and pd.notna(age_med_group.loc[key]):
            return age_med_group.loc[key]
        elif row['Маршрут'] in age_med_route.index and pd.notna(age_med_route.loc[row['Маршрут']]):
            return age_med_route.loc[row['Маршрут']]
        else:
            return age_med_all
