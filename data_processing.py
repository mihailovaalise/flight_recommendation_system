def calc_sparsity(df, user_col, item_col, name):
    n_users = df[user_col].nunique()
    n_items = df[item_col].nunique()
    n_interactions = df[[user_col, item_col]].drop_duplicates().shape[0]

    density = n_interactions / (n_users * n_items)
    sparsity = 1 - density

    print(f"=== {name} ===")
    print(f"Пользователей: {n_users}")
    print(f"Объектов: {n_items}")
    print(f"Уникальных взаимодействий: {n_interactions}")
    print(f"Плотность матрицы: {density:.8f}")
    print(f"Разреженность матрицы: {sparsity:.6f}")
    print()

    return {
        "name": name,
        "n_users": n_users,
        "n_items": n_items,
        "n_interactions": n_interactions,
        "density": density,
        "sparsity": sparsity
    }

