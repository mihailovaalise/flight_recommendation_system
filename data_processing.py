def plot_sparse_matrix(ax, df, user_col, item_col, title, top_n_users=200, top_n_items=200):
    top_users = df[user_col].value_counts().head(top_n_users).index
    top_items = df[item_col].value_counts().head(top_n_items).index

    df_plot = df[
        df[user_col].isin(top_users) &
        df[item_col].isin(top_items)
    ].copy()

    user_to_idx = {user: i for i, user in enumerate(top_users)}
    item_to_idx = {item: i for i, item in enumerate(top_items)}

    rows = df_plot[user_col].map(user_to_idx)
    cols = df_plot[item_col].map(item_to_idx)

    matrix = coo_matrix(
        (np.ones(len(df_plot)), (rows, cols)),
        shape=(len(top_users), len(top_items))
    )

    ax.spy(matrix, markersize=1)
    ax.set_title(title, fontsize=14)
    ax.set_xlabel('Объекты')
    ax.set_ylabel('Пассажиры')

